package HashMapAssignment;

public class HashMapStudent {
	String name;
	int age;
	String email;
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	
	public String getEmail() {
		return email;
	}
	public void setStudent(String email,String name,int age) {
		this.name = name;
		this.age= age;
		this.email = email;
	}
	

}
